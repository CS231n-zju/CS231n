  delta = 1.0
  scores = X.dot(W)
  train_num = X.shape[0]
  cor_scores = scores[np.arange(train_num), y]   # 注意取值的方式
  margin = scores - (cor_scores - delta).reshape(train_num, 1)   #注意符号
  margin[np.arange(train_num), y] = 0   # 正确分类置零
  margin = np.maximum(margin, 0)   # 负值取零
  loss = np.sum(margin)
  loss /= train_num
  loss += reg * np.sum(W * W)
